    private void tabelMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tabelMouseClicked
        Clear();
        try {
            int row = tabel.getSelectedRow();
            String tabel_klik = tabel.getModel().getValueAt(row, 0).toString();
            Connection con = Koneksi.connect();
            Statement st = con.createStatement();
            ResultSet rs = st.executeQuery("SELECT * FROM tbl_gaji_detail WHERE id='"+tabel_klik+"'");
            if(rs.next()) {
                id.setText(rs.getString("id"));
                id1.setText(rs.getString("id_gaji"));
                txtxNama.setText(rs.getString("nama"));
                txtGaji.setText(rs.getString("gaji"));
                cbStatus.setSelectedItem(rs.getString("status"));
                if(rs.getString("senin").equals("1")) cbSenin.setSelected(true);
                if(rs.getString("selasa").equals("1")) cbSelasa.setSelected(true);
                if(rs.getString("rabu").equals("1")) cbRabu.setSelected(true);
                if(rs.getString("kamis").equals("1")) cbKamis.setSelected(true);
                if(rs.getString("jumat").equals("1")) cbJumat.setSelected(true);
                if(rs.getString("sabtu").equals("1")) cbSabtu.setSelected(true);
                txtLembur.setText(rs.getString("lembur"));
                txtTotalLembur.setText(rs.getString("gaji_lembur"));
                txtBonus.setText(rs.getString("bonus"));
                txtPotongan.setText(rs.getString("potongan"));
                txtJumlah.setText(rs.getString("jumlah"));
            }
            btnSimpan.setText("Simpan");
        } catch (Exception e) {
            JOptionPane.showMessageDialog(rootPane, e);
        }
    }//GEN-LAST:event_tabelMouseClicked

