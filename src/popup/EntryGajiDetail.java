    public void loadData() {
       DefaultTableModel model = new DefaultTableModel();
        model.addColumn("ID");
        model.addColumn("NAMA");
        model.addColumn("KERJA");
        model.addColumn("LEMBUR");
        model.addColumn("GAJI LEMBUR");
        model.addColumn("BONUS");
        model.addColumn("POTONGAN");
        model.addColumn("TOTAL");
        try {
            Connection con = Koneksi.connect();
            Statement st = con.createStatement();
            ResultSet rs = st.executeQuery("SELECT * FROM tbl_gaji_detail WHERE id_gaji='"+id1.getText()+"'");
            while(rs.next()) {
               model.addRow(new Object[]{
               rs.getString("id"), rs.getString("nama"), rs.getString("hadir")+" HARI", rs.getString("lembur")+" HARI/JAM",
               rs.getString("gaji_lembur"), rs.getString("bonus"), rs.getString("potongan"), rs.getString("jumlah")});
               tabel.setModel(model);
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(rootPane, e);
        } 
    }
    
